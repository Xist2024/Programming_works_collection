async def measure_tcp_connection_async(ip, port):
    """异步测量 TCP 连接建立时间"""
    start_time = time.time()
    try:
        fut = asyncio.open_connection(ip, port)
        reader, writer = await asyncio.wait_for(fut, timeout=0.5)
        connect_time = (time.time() - start_time) * 1000
        timeout = 500
        writer.close()
        await writer.wait_closed()
        return connect_time, 0, timeout
    except (socket.error, asyncio.TimeoutError):
        return None, None, None

async def measure_udp_response_async(ip, port):
    """异步测量 UDP 响应时间"""
    start_time = time.time()
    s = socket.socket(socket.AF_INET, socket.SOCK_DGRAM)
    s.setblocking(False)
    try:
        loop = asyncio.get_event_loop()
        await loop.sock_sendto(s, b"test", (ip, port))
        await asyncio.wait_for(loop.sock_recv(s, 1024), timeout=0.5)
        return (time.time() - start_time) * 1000
    except (socket.error, asyncio.TimeoutError):
        return None
    finally:
        s.close()

