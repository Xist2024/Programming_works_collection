async def can_crawl(full_url):
    """检测页面是否可爬取"""
    ua = UserAgent()
    headers = {'User-Agent': ua.random}
    async with RetryClient() as session:
        try:
            async with session.head(full_url, headers=headers, timeout=aiohttp.ClientTimeout(total=5)) as response:
                if response.status == 200:
                    robots_txt_url = urljoin(full_url, '/robots.txt')
                    async with session.get(robots_txt_url, timeout=aiohttp.ClientTimeout(total=2)) as robots_response:
                        if robots_response.status == 200:
                            robots_txt = await robots_response.text()
                            if 'Disallow: /' in robots_txt:
                                return False
                    return True
                return False
        except Exception:
            return False

async def crawl_website(full_url, domain):
    """爬取目标网站的静态资源和源代码，保存到域名的子文件夹"""
    ua = UserAgent()
    headers = {'User-Agent': ua.random}
    resources = {'images': [], 'videos': [], 'css': [], 'js': []}
    timestamp = datetime.now().strftime("%Y-%m-%d_%H-%M-%S")
    domain_dir = os.path.join(WEBBUG_DIR, domain)
    os.makedirs(domain_dir, exist_ok=True)

    async with RetryClient() as session:
        try:
            async with session.get(full_url, headers=headers, timeout=aiohttp.ClientTimeout(total=10)) as response:
                if response.status == 200:
                    html = await response.text()
                    soup = BeautifulSoup(html, 'html.parser')

                    # 保存源代码
                    source_file = os.path.join(domain_dir, f"{domain}_源代码_{timestamp}.txt")
                    async with aiofiles.open(source_file, 'w', encoding='utf-8') as f:
                        await f.write(html)

                    # 提取静态资源
                    for img in soup.find_all('img'):
                        src = img.get('src')
                        if src:
                            resources['images'].append(urljoin(full_url, src))
                    for video in soup.find_all('video'):
                        src = video.get('src') or (video.find('source', src=True).get('src') if video.find('source') else None)
                        if src:
                            resources['videos'].append(urljoin(full_url, src))
                    for link in soup.find_all('link', rel='stylesheet'):
                        href = link.get('href')
                        if href:
                            resources['css'].append(urljoin(full_url, href))
                    for script in soup.find_all('script', src=True):
                        src = script.get('src')
                        if src:
                            resources['js'].append(urljoin(full_url, src))

                    total_resources = sum(len(lst) for lst in resources.values())
                    tasks = []
                    semaphore = asyncio.Semaphore(20)  # 提高并发限制以加快下载

                    async def download_resource(url, resource_type, pbar):
                        async with semaphore:
                            try:
                                async with session.get(url, headers=headers, timeout=aiohttp.ClientTimeout(total=10)) as resp:
                                    if resp.status == 200:
                                        content = await resp.read()
                                        ext = url.split('.')[-1].split('?')[0]
                                        filename = f"{domain}_{resource_type}_{timestamp}.{ext}"
                                        filepath = os.path.join(domain_dir, filename)
                                        async with aiofiles.open(filepath, 'wb') as f:
                                            await f.write(content)
                                        pbar.update(1)
                            except Exception:
                                pass

                    with tqdm(total=total_resources, desc=f"{GREEN}爬取 {domain} 资源{RESET}", unit="file", leave=False) as pbar:
                        for resource_type, urls in resources.items():
                            for idx, url in enumerate(urls):
                                resource_name = f"{resource_type[:-1]}{idx + 1}"
                                tasks.append(download_resource(url, resource_name, pbar))
                        await asyncio.gather(*tasks)

                    return total_resources, os.path.getsize(source_file) if os.path.exists(source_file) else 0
                return 0, 0
        except Exception as e:
            print(f"{GREEN}爬取失败: {str(e)}{RESET}")
            return 0, 0
    return 0, 0

