def guess_server_software(status):
    """根据版本或 MOTD 猜测服务器软件"""
    version = status.version.name.lower()
    motd = status.description.lower()
    if "spigot" in version or "spigot" in motd:
        return "Spigot"
    elif "bukkit" in version or "bukkit" in motd:
        return "Bukkit"
    elif "paper" in version or "paper" in motd:
        return "Paper"
    elif "forge" in version or "forge" in motd:
        return "Forge"
    else:
        return "Vanilla 或未知"

